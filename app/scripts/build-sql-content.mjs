// scripts/build-sql-content.mjs
// At build time, read the Round-1 sql/ files from the repo root and inline them
// into src/lib/sql-content.ts as string constants. The deployed app does not
// access ../sql/ at runtime (Vercel only ships files under the project root).

import fs from 'node:fs';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const SQL_DIR = path.resolve(__dirname, '..', '..', 'sql');
const OUT = path.resolve(__dirname, '..', 'src', 'lib', 'sql-content.ts');

const exists = fs.existsSync(SQL_DIR);
const entries = exists
  ? fs.readdirSync(SQL_DIR).filter((f) => f.endsWith('.sql')).sort()
  : [];

const lines = [
  '// AUTO-GENERATED by scripts/build-sql-content.mjs — do not edit.',
  '// Inlines Round-1 sql/ files so the AI page and methodology page can show',
  '// source queries without runtime fs access (Vercel ships project root only).',
  '',
  'export const SQL_FILES: Record<string, string> = {',
];

for (const f of entries) {
  const body = fs.readFileSync(path.join(SQL_DIR, f), 'utf8');
  lines.push(`  ${JSON.stringify(f)}: ${JSON.stringify(body)},`);
}

lines.push('};', '');
lines.push('export const SQL_FILE_LIST = Object.keys(SQL_FILES);', '');

fs.mkdirSync(path.dirname(OUT), { recursive: true });
fs.writeFileSync(OUT, lines.join('\n'));
console.log(`[sql-content] wrote ${entries.length} files → ${path.relative(process.cwd(), OUT)}`);
